rows = [[symbol for symbol in line] for line in input.split("\n")]
visited = [[False] * len(rows[0]) for i in range(len(rows))]

#stops before counting last 2 visited spots
ans = 2

#find starting point and mark visited
for i in range(len(rows)):
    for j in range(len(rows[0])):
        if rows[i][j] == '^':
            row = i
            col = j
            visited[i][j] = True
            
while (row != len(rows[0]) - 1) and (row != 0) and (col != len(rows) - 1) and (col != 0):

    #travel up
    while row != 1 and rows[row-1][col] != '#':
        visited[row][col] = True
        row -= 1  
    if row == 1 and rows[row-1][col] != '#':
        break
    
    #travel right
    while col != len(rows[0]) - 2 and rows[row][col+1] != '#':
        visited[row][col] = True
        col += 1
    if col == len(rows[0]) - 2 and rows[row][col+1] != '#':
        break

    #travel down
    while row != len(rows) - 2 and rows[row+1][col] != '#':
        visited[row][col] = True
        row += 1
    if row == len(rows) - 2 and rows[row+1][col] != '#':
        break

    #travel left    
    while col != 1 and rows[row][col-1] != '#':
        visited[row][col] = True
        col -= 1
    if col == 1 and rows[row][col-1] != '#':
        break

#count visited points
for row in visited:
    for val in row:
        if val == True:
            ans += 1

print(ans)




         
